// 問題 3.8 💻
console.log("to number");
console.log("`true` :" + Number(true));
console.log("`1234` :" + Number(1234));
console.log("`'text'` :" + Number("text"));

console.log("to boolean");
console.log("`1234` :" + Boolean(1234));
console.log("`0` :" + Boolean(0));

console.log("to string");
console.log("`true` :" + String(true));
console.log("`1234` :" + String(1234));

console.log("to parseInt");
console.log("'12,742 km：地球の直径' :" + parseInt("12,742 km：地球の直径"));

console.log("to parseFloat");
console.log("'1.618：黄金比' :" + parseFloat("1.618：黄金比"));
